/**
 * Generate the chatbot's pinned "identity core" from the SAME data files the
 * website and the RAG corpus use, so there is ONE source of truth. Adding a
 * project to data/projectsData.ts (or a role/publication to data/aboutData.js)
 * flows into the pinned system prompt automatically — no hand-copied second
 * copy to drift out of sync.
 *
 * Writes worker/src/identity-core.generated.ts (imported by worker/src/prompts.ts).
 * Runs standalone (`npm run gen:identity`) and at the start of `npm run ingest`.
 *
 * Sources:
 *   - data/projectsData.ts  -> Projects (title, description|blurb, href)
 *   - data/aboutData.js     -> Publication + Experience
 *   - data/siteMetadata.js  -> Contact (email, github, linkedin)
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import aboutData from '../data/aboutData.js'
import siteMetadata from '../data/siteMetadata.js'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..')
const SITE_URL = 'https://ashimsharma10.github.io'

// Style rule from the prompt: no em/en-dashes (an AI tell). Applied to all
// generated prose so the pinned core never reintroduces them.
const noDash = (s) => (s || '').replace(/\s*[—–]\s*/g, ', ')
const stripProto = (u) => (u || '').replace(/^https?:\/\//, '').replace(/\/$/, '')

// --- Projects (regex-parsed, mirrors scripts/ingest-knowledge.mjs) ---
const src = fs.readFileSync(path.join(ROOT, 'data/projectsData.ts'), 'utf8')
const objects = src.match(/\{[^}]*?title:[\s\S]*?\}/g) || []
const projects = objects
  .map((obj) => ({
    title: (obj.match(/title:\s*'([^']*)'/) || [])[1],
    // Prefer an optional short `blurb:` for the pinned line; fall back to description.
    blurb: (obj.match(/blurb:\s*\n?\s*'([^']*)'/) || [])[1],
    description: (obj.match(/description:\s*\n?\s*'([^']*)'/) || [])[1],
    href: (obj.match(/href:\s*'([^']*)'/) || [])[1],
  }))
  .filter((p) => p.title)

if (projects.length < 3 || projects.some((p) => (!p.blurb && !p.description) || !p.href)) {
  console.error(
    `gen-identity-core: projectsData.ts parse failed (got ${projects.length}). Aborting.`
  )
  process.exit(1)
}

const projectLines = projects
  .map((p) => {
    const url = p.href.startsWith('http') ? p.href : `${SITE_URL}${p.href}`
    return `- ${p.title}: ${noDash(p.blurb || p.description)} ${url}`
  })
  .join('\n')

// --- Publication (first entry) ---
const pub = (aboutData.publications || [])[0]
const pubLine = pub
  ? `- ${noDash((pub.citationParts || []).map((c) => c.text).join(''))} ${noDash(pub.venue || '')} ${pub.url || ''}`.trim()
  : ''

// --- Experience ---
const experience = (aboutData.experience || []).map((j) => `${j.role} at ${j.company}`).join('; ')

// --- Contact ---
const contact = [
  siteMetadata.email,
  stripProto(siteMetadata.github),
  stripProto(siteMetadata.linkedin),
]
  .filter(Boolean)
  .join(' · ')

const identityCore = `Core facts about Ashim (authoritative, you always know these, no search needed):

Projects:
${projectLines}

Publication:
${pubLine}

Experience: ${noDash(experience).replace(/\.+$/, '')}.

Contact: ${contact}`

// Escape for safe embedding in a TS template literal.
const escaped = identityCore.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${')

const out = `// AUTO-GENERATED by scripts/gen-identity-core.mjs — do not edit by hand.
// Regenerate with: npm run gen:identity  (also runs at the start of npm run ingest)
export const IDENTITY_CORE = \`${escaped}\`
`

const dest = path.join(ROOT, 'worker/src/identity-core.generated.ts')
fs.writeFileSync(dest, out)
console.log(
  `gen-identity-core: wrote ${projects.length} projects + publication + experience -> ${path.relative(ROOT, dest)}`
)

if (process.env.PRINT) console.log('\n' + identityCore)
